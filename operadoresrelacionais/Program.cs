// ESTUDAR A TABELA VERDADE
//REGRA
// (e) conjunção será verdadeira somente quando todas as proposições forem verdadeira
Console.WriteLine(false && true); // F
Console.WriteLine(false && false); // F
Console.WriteLine(true && true); // V


//REGRA
//(ou) disjunção será verdadeira quando pelo menos uma das proposições for verdadeira
Console.WriteLine(false || true); // V
Console.WriteLine(false || false); // F
Console.WriteLine(true || true); // V


